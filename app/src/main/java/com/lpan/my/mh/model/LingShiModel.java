public class LingShiModel {

    private int jingshi;//晶石
    private int shouzhuo;//手镯
    private int peishi;//佩饰
    private int ershi;//耳饰
    private int jiezhi;//戒指
    private double cost;

    public int getJingshi() {
        return jingshi;
    }

    public void setJingshi(int jingshi) {
        this.jingshi = jingshi;
    }

    public int getShouzhuo() {
        return shouzhuo;
    }

    public void setShouzhuo(int shouzhuo) {
        this.shouzhuo = shouzhuo;
    }

    public int getPeishi() {
        return peishi;
    }

    public void setPeishi(int peishi) {
        this.peishi = peishi;
    }

    public int getErshi() {
        return ershi;
    }

    public void setErshi(int ershi) {
        this.ershi = ershi;
    }

    public int getJiezhi() {
        return jiezhi;
    }

    public void setJiezhi(int jiezhi) {
        this.jiezhi = jiezhi;
    }

    public double getCost() {
        return cost;
    }

    public void setCost(double cost) {
        this.cost = cost;
    }
}
